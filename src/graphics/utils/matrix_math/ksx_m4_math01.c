#include "ksx_graphics.h"
#include "ksx_vec3_math.h"
#include "ksx_m4_math.h"
#include <math.h>

void	ksx_m4_reset(t_matrix4 *p_m4)
{
	p_m4->e_11 = 0.f;
	p_m4->e_12 = 0.f;
	p_m4->e_13 = 0.f;
	p_m4->e_14 = 0.f;
	p_m4->e_21 = 0.f;
	p_m4->e_22 = 0.f;
	p_m4->e_23 = 0.f;
	p_m4->e_24 = 0.f;
	p_m4->e_31 = 0.f;
	p_m4->e_32 = 0.f;
	p_m4->e_33 = 0.f;
	p_m4->e_34 = 0.f;
	p_m4->e_41 = 0.f;
	p_m4->e_42 = 0.f;
	p_m4->e_43 = 0.f;
	p_m4->e_44 = 0.f;
}

static void	ksx_m4_multi_(const t_matrix4 m4_1,
			const t_matrix4 m4_2, t_matrix4 *p_m4);

t_matrix4	ksx_m4_multi(const t_matrix4 m4_1, const t_matrix4 m4_2)
{
	t_matrix4	m4;

	m4.e_11 = m4_1.e_11 * m4_2.e_11 + m4_1.e_12 * m4_2.e_21
		+ m4_1.e_13 * m4_2.e_31 + m4_1.e_14 * m4_2.e_41;
	m4.e_12 = m4_1.e_11 * m4_2.e_12 + m4_1.e_12 * m4_2.e_22
		+ m4_1.e_13 * m4_2.e_32 + m4_1.e_14 * m4_2.e_42;
	m4.e_13 = m4_1.e_11 * m4_2.e_13 + m4_1.e_12 * m4_2.e_23
		+ m4_1.e_13 * m4_2.e_33 + m4_1.e_14 * m4_2.e_43;
	m4.e_14 = m4_1.e_11 * m4_2.e_14 + m4_1.e_12 * m4_2.e_24
		+ m4_1.e_13 * m4_2.e_34 + m4_1.e_14 * m4_2.e_44;
	m4.e_21 = m4_1.e_21 * m4_2.e_11 + m4_1.e_22 * m4_2.e_21
		+ m4_1.e_23 * m4_2.e_31 + m4_1.e_24 * m4_2.e_41;
	m4.e_22 = m4_1.e_21 * m4_2.e_12 + m4_1.e_22 * m4_2.e_22
		+ m4_1.e_23 * m4_2.e_32 + m4_1.e_24 * m4_2.e_42;
	m4.e_23 = m4_1.e_21 * m4_2.e_13 + m4_1.e_22 * m4_2.e_23
		+ m4_1.e_23 * m4_2.e_33 + m4_1.e_24 * m4_2.e_43;
	m4.e_24 = m4_1.e_21 * m4_2.e_14 + m4_1.e_22 * m4_2.e_24
		+ m4_1.e_23 * m4_2.e_34 + m4_1.e_24 * m4_2.e_44;
	ksx_m4_multi_(m4_1, m4_1, &m4);
	return (m4);
}

static void	ksx_m4_multi_(const t_matrix4 m4_1,
					const t_matrix4 m4_2, t_matrix4 *p_m4)
{
	p_m4->e_31 = m4_1.e_31 * m4_2.e_11 + m4_1.e_32 * m4_2.e_21
		+ m4_1.e_33 * m4_2.e_31 + m4_1.e_34 * m4_2.e_41;
	p_m4->e_32 = m4_1.e_31 * m4_2.e_12 + m4_1.e_32 * m4_2.e_22
		+ m4_1.e_33 * m4_2.e_32 + m4_1.e_34 * m4_2.e_42;
	p_m4->e_33 = m4_1.e_31 * m4_2.e_13 + m4_1.e_32 * m4_2.e_23
		+ m4_1.e_33 * m4_2.e_33 + m4_1.e_34 * m4_2.e_43;
	p_m4->e_34 = m4_1.e_31 * m4_2.e_14 + m4_1.e_32 * m4_2.e_24
		+ m4_1.e_33 * m4_2.e_34 + m4_1.e_34 * m4_2.e_44;
	p_m4->e_41 = m4_1.e_41 * m4_2.e_11 + m4_1.e_42 * m4_2.e_21
		+ m4_1.e_43 * m4_2.e_31 + m4_1.e_44 * m4_2.e_41;
	p_m4->e_42 = m4_1.e_41 * m4_2.e_12 + m4_1.e_42 * m4_2.e_22
		+ m4_1.e_43 * m4_2.e_32 + m4_1.e_44 * m4_2.e_42;
	p_m4->e_43 = m4_1.e_41 * m4_2.e_13 + m4_1.e_42 * m4_2.e_23
		+ m4_1.e_43 * m4_2.e_33 + m4_1.e_44 * m4_2.e_43;
	p_m4->e_44 = m4_1.e_41 * m4_2.e_14 + m4_1.e_42 * m4_2.e_24
		+ m4_1.e_43 * m4_2.e_34 + m4_1.e_44 * m4_2.e_44;
}

t_vector4	ksx_m4_vec4(const t_matrix4 m, const t_vector4 v)
{
	t_vector4	v4;

	v4.x = (m.e_11 * v.x) + (m.e_12 * v.y) + (m.e_13 * v.z) + (m.e_14 * v.w);
	v4.y = (m.e_21 * v.x) + (m.e_22 * v.y) + (m.e_23 * v.z) + (m.e_24 * v.w);
	v4.z = (m.e_31 * v.x) + (m.e_32 * v.y) + (m.e_33 * v.z) + (m.e_34 * v.w);
	v4.w = (m.e_41 * v.x) + (m.e_42 * v.y) + (m.e_43 * v.z) + (m.e_44 * v.w);
	return (v4);
}
